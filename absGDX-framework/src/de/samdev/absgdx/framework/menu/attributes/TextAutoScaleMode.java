package de.samdev.absgdx.framework.menu.attributes;

/**
 * How to scale the Text in a Label/Button/Edit ...
 */
public enum TextAutoScaleMode {
	/** Use the manual set fontscale */
	NONE,
	/** Scale to fit the height */
	VERTICAL,
	/** Scale to fit the width */
	HORIZONTAL,
	/** Scale to fit the height and width */
	BOTH;
}
